### Create Age Bins
ALTER TABLE ecomm ADD COLUMN Age_Bin VARCHAR(20);

UPDATE ecomm
SET Age_Bin = CASE
    WHEN Age BETWEEN 18 AND 24 THEN '18-24'
    WHEN Age BETWEEN 25 AND 34 THEN '25-34'
    WHEN Age BETWEEN 35 AND 44 THEN '35-44'
    WHEN Age BETWEEN 45 AND 54 THEN '45-54'
    WHEN Age >= 55 THEN '55+'
    ELSE 'Unknown'
END;

select * from ecomm